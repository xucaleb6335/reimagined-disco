static const uint8_t *glyph(char ch)
{
    if (ch >= 'a' && ch <= 'z')
        ch = (char)(ch - 'a' + 'A');
    switch (ch) {
    case ':': return font5x7[1];
    case '.': return font5x7[2];
    case '/': return font5x7[3];
    case '-': return font5x7[4];
    case '%': return font5x7[5];
    case '=': return font5x7[6];
    default:
        if (ch >= '0' && ch <= '9')
            return font5x7[7 + (ch - '0')];
        if (ch >= 'A' && ch <= 'Z')
            return font5x7[17 + (ch - 'A')];
        return font5x7[0];
    }
}

void vga_putc(int x, int y, char ch, uint16_t fg, uint16_t bg)
{
    const uint8_t *g = glyph(ch);
    for (int row = 0; row < VGA_FONT_H; row++)
        for (int col = 0; col < VGA_FONT_W; col++)
            vga_pixel(x + col, y + row,
                      (g[row] >> (7 - col)) & 1 ? fg : bg);
}

void vga_text(int x, int y, const char *s, uint16_t fg, uint16_t bg)
{
    for (; *s; s++, x += VGA_FONT_W)
        vga_putc(x, y, *s, fg, bg);
}

void vga_color_bars(void)
{
    static const uint16_t bars[8] = {
        VGA_WHITE, VGA_YELLOW, VGA_CYAN, VGA_GREEN,
        VGA_MAGENTA, VGA_RED, VGA_BLUE, VGA_BLACK
    };
    int w = VGA_WIDTH / 8;
    for (int i = 0; i < 8; i++)
        vga_fill_rect(i * w, 0, w, VGA_HEIGHT, bars[i]);
}
