#include <unistd.h>
#endif

static uint8_t *fb; //byte pointer; pixel at (y << VGA_Y_SHIFT) + (x << 1)

#ifndef BARE_METAL
static int vga_fd = -1;
static void *vga_map;
#endif

int vga_open(void)
{
#ifdef BARE_METAL
    fb = (uint8_t *)VGA_PIXBUF_PHYS;
    return 0;
#else
    vga_fd = open("/dev/mem", O_RDWR | O_SYNC);
    if (vga_fd < 0) {
        perror("open /dev/mem (are you root?)");
        return -1;
    }

    vga_map = mmap(NULL, VGA_SPAN, PROT_READ | PROT_WRITE,
                   MAP_SHARED, vga_fd, VGA_PIXBUF_PHYS);
    if (vga_map == MAP_FAILED) {
        perror("mmap VGA pixel buffer");
        close(vga_fd);
        vga_fd = -1;
        return -1;
    }

    fb = (uint8_t *)vga_map;
    return 0;
#endif
}

void vga_close(void)
{
#ifndef BARE_METAL
    if (vga_map)
        munmap(vga_map, VGA_SPAN);
    if (vga_fd >= 0)
        close(vga_fd);
    vga_map = NULL;
    vga_fd = -1;
#endif
    fb = NULL;
}

void vga_attach(void *framebuffer)
{
    fb = (uint8_t *)framebuffer;
}

void vga_pixel(int x, int y, uint16_t color)
{
    if (!fb || x < 0 || x >= VGA_WIDTH || y < 0 || y >= VGA_HEIGHT)
        return;
    *(volatile uint16_t *)(fb + (y << VGA_Y_SHIFT) + (x << 1)) = color;
}

void vga_fill_rect(int x, int y, int w, int h, uint16_t color)
{
    for (int j = y; j < y + h; j++)
        for (int i = x; i < x + w; i++)
            vga_pixel(i, j, color);
}

void vga_clear(uint16_t color)
{
    vga_fill_rect(0, 0, VGA_WIDTH, VGA_HEIGHT, color);
}

uint16_t vga_heatmap(int32_t v, int32_t vmax)
{
    if (v == 0)
        return VGA_BLACK;
    if (vmax <= 0)
        vmax = 1;

    //t = 0..255 magnitude relative to vmax
    int64_t mag = v < 0 ? -(int64_t)v : (int64_t)v;
    int t = (int)((mag * 255) / vmax);
    if (t > 255)
        t = 255;

    int r = 0, g = 0, b = 0;
    if (v > 0) {
        //black -> red -> yellow
        r = t < 128 ? t * 2 : 255;
        g = t < 128 ? 0 : (t - 128) * 2;
    } else {
        //black -> blue -> cyan
        b = t < 128 ? t * 2 : 255;
        g = t < 128 ? 0 : (t - 128) * 2;
    }
    return VGA_RGB(r >> 3, g >> 2, b >> 3);
}

